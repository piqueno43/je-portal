var jq = jQuery.noConflict();
    jq(document).ready(function() {
         var audio;
         var playlist;
         var tracks;
         var current;
         init();

         function init() {
             current = 0;
             audio = jq('audio');
             playlist = jq('#playlist');
             tracks = playlist.find('.titulo_divulga');
             len = tracks.length - 1;
             audio[0].volume = .60;
             playlist.find('a')
                 .click(function(e) {
                     e.preventDefault();
                     link = jq(this);
                     current = link.parent()
                         .index();
                     run(link, audio[0]);
                 });
             audio[0].addEventListener('ended', function(e) {
                 current++;
                 if (current == len) {
                     current = 0;
                     link = playlist.find('a')[0];
                 } else {
                     link = playlist.find('a')[current];
                 }
                 run(jq(link), audio[0]);
             });
         }

         function run(link, player) {
             player.src = link.attr('href');
             texto_audio = link.find('.texto-audio')
                 .text();
             jq('#audio-texto')
                 .html(texto_audio);
             par = link.parent();
             par.addClass('active')
                 .siblings()
                 .removeClass('active');
             audio[0].load();
             audio[0].play();
         }
     });
